# ===== DESAFIO 01 - SOMANDO DOIS NÚMEROS =====

# Exibe um título na tela
print('===== DESAFIO 01 - SOMANDO DOIS NÚMEROS =====')

# Solicita que o usuário digite o primeiro número
n1 = float(input('Digite seu número: '))

# Solicita que o usuário digite o segundo número
n2 = float(input('Digite seu outro número: '))

# Realiza a soma dos dois números informados
soma = n1 + n2

# Exibe o resultado da soma de forma organizada
print('A soma entre {} e {} é {}'.format(n1, n2, soma))
