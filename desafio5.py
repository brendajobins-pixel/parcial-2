# ===== DESAFIO 05 - CALCULANDO A ÁREA DE UM TRIÂNGULO =====

# Exibe um título na tela
print('===== DESAFIO 05 - CALCULANDO A ÁREA DE UM TRIÂNGULO =====')

# Solicita que o usuário digite a base do triângulo
base = float(input('Qual a base do seu triângulo: '))

# Solicita que o usuário digite a altura do triângulo
altura = float(input('Qual a altura do seu triângulo: '))

# Calcula a área do triângulo
# Fórmula: (base * altura) / 2
area = (base * altura) / 2

# Exibe o resultado
print('A área do triângulo é:', area)
